// Функция для заполнения шаблона данными
function fillTemplate(template, order) {
    return template
        .replace(/{name}/g, order.name)
        .replace(/{purpose}/g, order.purpose)
        .replace(/{budget}/g, order.budget.toLocaleString('ru-RU'))
        .replace(/{id}/g, order.id);
}